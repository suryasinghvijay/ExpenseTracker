
DROP TABLE IF EXISTS EXPENSES CASCADE ;
CREATE TABLE IF NOT EXISTS EXPENSES (
   ID SERIAL PRIMARY KEY,
   UUID VARCHAR (200) NOT NULL,
   DETAILS VARCHAR(50) NOT NULL,
   TRANSACTION_AMOUNT DECIMAL,
   TRANSACTION_ACCOUNT VARCHAR(100) NOT NULL,
   TRANSACTION_TYPE VARCHAR(100) NOT NULL,
   TRANSACTION_DATE DATE NOT NULL,
   CREATED_AT TIMESTAMP Not NULL
);
COMMENT ON TABLE EXPENSES IS 'Table to capture expenses';
COMMENT ON COLUMN EXPENSES.ID IS 'Auto generated PK identifier';
COMMENT ON COLUMN EXPENSES.UUID IS 'Unique identifier used as reference by external systems';
COMMENT ON COLUMN EXPENSES.DETAILS IS 'expenses details';
COMMENT ON COLUMN EXPENSES.TRANSACTION_AMOUNT IS 'amount spent';
COMMENT ON COLUMN EXPENSES.TRANSACTION_ACCOUNT IS 'Type of account';
COMMENT ON COLUMN EXPENSES.TRANSACTION_TYPE IS 'Type of transaction';
COMMENT ON COLUMN EXPENSES.TRANSACTION_DATE IS 'captured date transaction';


COMMIT;



